class StyleTransform(object):
    def __init__(self, style_path, vgg, decoder, transfer_prob=0.5, 
                 style_mode='list', content_size=0, style_size=0, dataset=None):
        # self.style_path = style_path
        self.style_mode = style_mode
        self.transfer_prob = transfer_prob
        self.vgg = vgg
        self.decoder = decoder
        self.content_size = content_size
        self.style_size = style_size
        self.dataset = dataset
        if self.style_mode == 'list':
            self.style_path = get_path_list(style_path)
        elif self.style_mode == 'dict':
            self.style_path = get_path_dict(style_path)
        else:
            raise NotImplementedError()

    def __call__(self, img, class_name=None):
        if np.random.rand() > self.transfer_prob:
            return img
        else:
            if self.style_mode == 'list':
                return list_style_transfer(img, self.style_path, self.vgg, self.decoder, 
                                           self.content_size, self.style_size, self.dataset)
            elif self.style_mode == 'dict':
                return dict_style_transfer(img, class_name, self.style_path, self.vgg, self.decoder, 
                                           self.content_size, self.style_size, self.dataset)
            else:
                raise NotImplementedError()

    def __repr__(self):
        return self.__class__.__name__ + '(prob={0}, style_path={1})'.format(self.transfer_prob, self.style_path)


def get_mean_and_std(dataset):
    '''Compute the mean and std value of dataset.'''
    dataloader = torch.utils.data.DataLoader(dataset, batch_size=1, shuffle=True, num_workers=2)
    mean = torch.zeros(3)
    std = torch.zeros(3)
    print('==> Computing mean and std..')
    for inputs, targets in dataloader:
        for i in range(3):
            mean[i] += inputs[:, i, :, :].mean()
            std[i] += inputs[:, i, :, :].std()
    mean.div_(len(dataset))
    std.div_(len(dataset))
    return mean, std


def init_params(net):
    '''Init layer parameters.'''
    for m in net.modules():
        if isinstance(m, nn.Conv2d):
            init.kaiming_normal(m.weight, mode='fan_out')
            if m.bias:
                init.constant(m.bias, 0)
        elif isinstance(m, nn.BatchNorm2d):
            init.constant(m.weight, 1)
            init.constant(m.bias, 0)
        elif isinstance(m, nn.Linear):
            init.normal(m.weight, std=1e-3)
            if m.bias:
                init.constant(m.bias, 0)


